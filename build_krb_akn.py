def _xml_escape(s: str) -> str:
    return (s.replace("&", "&amp;")
             .replace("<", "&lt;")
             .replace(">", "&gt;")
             .replace('"', "&quot;"))


def build_xml() -> str:
    lines = []
    lines.append('<?xml version="1.0" encoding="UTF-8"?>')
    lines.append('<akomaNtoso xmlns="http://docs.oasis-open.org/legaldocml/ns/akn/3.0">')
    lines.append('  <act contains="originalVersion">')
    lines.append('    <meta>')
    lines.append('      <identification source="#openlegis">')
    lines.append('        <FRBRWork>')
    lines.append('          <FRBRthis value="/akn/bg/act/1991/krb/main"/>')
    lines.append('          <FRBRuri value="/akn/bg/act/1991/krb"/>')
    lines.append('          <FRBRalias value="Конституция на Република България" name="short"/>')
    lines.append('          <FRBRalias value="Конституция на Република България" name="eli" other="/eli/bg/konstitutsiya/1991/krb"/>')
    lines.append('          <FRBRdate date="1991-07-13" name="Generation"/>')
    lines.append('          <FRBRauthor href="#parliament"/>')
    lines.append('          <FRBRcountry value="bg"/>')
    lines.append('          <FRBRnumber value="1"/>')
    lines.append('        </FRBRWork>')
    lines.append('        <FRBRExpression>')
    lines.append('          <FRBRthis value="/akn/bg/act/1991/krb/bul@1991-07-13/main"/>')
    lines.append('          <FRBRuri value="/akn/bg/act/1991/krb/bul@1991-07-13"/>')
    lines.append('          <FRBRdate date="1991-07-13" name="Generation"/>')
    lines.append('          <FRBRauthor href="#parliament"/>')
    lines.append('          <FRBRlanguage language="bul"/>')
    lines.append('        </FRBRExpression>')
    lines.append('        <FRBRManifestation>')
    lines.append('          <FRBRthis value="/akn/bg/act/1991/krb/bul@1991-07-13/main.xml"/>')
    lines.append('          <FRBRuri value="/akn/bg/act/1991/krb/bul@1991-07-13.xml"/>')
    lines.append('          <FRBRdate date="1991-07-13" name="Generation"/>')
    lines.append('          <FRBRauthor href="#parliament"/>')
    lines.append('          <FRBRformat value="application/akn+xml"/>')
    lines.append('        </FRBRManifestation>')
    lines.append('      </identification>')
    lines.append('      <publication date="1991-07-13" name="Държавен вестник" number="56" showAs="ДВ"/>')
    lines.append('      <references source="#openlegis">')
    lines.append('        <TLCOrganization eId="parliament" href="/ontology/organization/bg/VNS" showAs="Велико народно събрание"/>')
    lines.append('        <TLCPerson eId="openlegis" href="/ontology/person/openlegis" showAs="open-legis"/>')
    lines.append('      </references>')
    lines.append('    </meta>')

    # Preface
    lines.append('    <preface>')
    lines.append('      <p>Конституция на Република България</p>')
    lines.append(f'      <p>(Обн., ДВ, бр. 56 от 13 юли 1991 г.)</p>')
    lines.append(f'      <p>{_xml_escape(PREAMBLE)}</p>')
    lines.append('    </preface>')

    lines.append('    <body>')

    # Chapters (Глави)
    chapter_roman = [
        "I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X"
    ]

    for ci, chapter in enumerate(CHAPTERS, start=1):
        roman = chapter_roman[ci - 1]
        ch_eid = f"chapter_{roman}"
        ch_num_escaped = _xml_escape(chapter["num"])
        ch_head_escaped = _xml_escape(chapter["heading"])
        lines.append(f'      <chapter eId="{ch_eid}">')
        lines.append(f'        <num>{ch_num_escaped}</num>')
        lines.append(f'        <heading>{ch_head_escaped}</heading>')

        for art in chapter["articles"]:
            # Derive article number for eId: "Чл. 1." → "art_1"
            num_str = art["num"]  # e.g. "Чл. 13."
            art_n = num_str.replace("Чл.", "").replace(".", "").strip()
            art_eid = f"art_{art_n}"

            lines.append(f'        <article eId="{art_eid}">')
            num_escaped = _xml_escape(num_str)
            lines.append(f'          <num>{num_escaped}</num>')
            if art["heading"]:
                lines.append(f'          <heading>{_xml_escape(art["heading"])}</heading>')

            paras = art["paras"]
            if len(paras) == 1 or not paras[0].startswith("("):
                # Single content block
                lines.append(f'          <content>')
                for para in paras:
                    lines.append(f'            <p>{_xml_escape(para)}</p>')
                lines.append(f'          </content>')
            else:
                # Multiple алинеи
                for pi, para in enumerate(paras, start=1):
                    para_eid = f"{art_eid}__para_{pi}"
                    # Extract paragraph number if present
                    if para.startswith("("):
                        # Find the closing paren
                        end = para.index(")")
                        para_num = para[:end + 1]
                        para_text = para[end + 1:].strip()
                    else:
                        para_num = f"({pi})"
                        para_text = para
                    lines.append(f'          <paragraph eId="{para_eid}">')
                    lines.append(f'            <num>{_xml_escape(para_num)}</num>')
                    lines.append(f'            <content><p>{_xml_escape(para_text)}</p></content>')
                    lines.append(f'          </paragraph>')

            lines.append(f'        </article>')

        lines.append(f'      </chapter>')

    # Transitional provisions
    lines.append('      <hcontainer eId="conclusions" name="transitional">')
    lines.append('        <heading>ПРЕХОДНИ И ЗАКЛЮЧИТЕЛНИ РАЗПОРЕДБИ</heading>')
    for trans in TRANSITIONAL:
        num_str = trans["num"]
        par_n = num_str.replace("§", "").replace(".", "").strip()
        par_eid = f"conclusions__trans_{par_n}"
        lines.append(f'        <hcontainer eId="{par_eid}" name="provision">')
        lines.append(f'          <num>{_xml_escape(num_str)}</num>')
        paras = trans["paras"]
        lines.append(f'          <content>')
        for para in paras:
            lines.append(f'            <p>{_xml_escape(para)}</p>')
        lines.append(f'          </content>')
        lines.append(f'        </hcontainer>')
    lines.append('      </hcontainer>')

    lines.append('    </body>')
    lines.append('  </act>')
    lines.append('</akomaNtoso>')

    return "\n".join(lines)


